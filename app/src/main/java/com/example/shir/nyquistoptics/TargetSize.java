package com.example.shir.nyquistoptics;

public class TargetSize {

    private	int	width;
    private	int	height;

    public TargetSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

     public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }


}
